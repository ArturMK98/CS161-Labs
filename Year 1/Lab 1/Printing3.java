public class Printing3 {

    public static void main (String args []) {
        
        // The system will print '3 + 4' to the screen
        System.out.println("3 + 4");
        
        // The system will print the sum of '3' and '4' to the screen
        System.out.println(3 + 4);
            
        // The system will print '3' with a '4' right after it
        System.out.println(3 + "4");
    }
}