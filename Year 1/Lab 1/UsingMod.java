public class PrintingVariables {
    public static void main (String args []) {

        // The value of 'num1' is 5
        int num1 = 5;
          
        // The value of 'num2' is 2.3
        double num2 = 2.3;
            
        // The value of 'letter' is s
        char letter = 's';
              
        // The system will print the value of 'num1' to the screen
        System.out.println(num1);
              
        // The system will print the value of 'num2' to the screen
        System.out.println(num2);
                  
        // The system will print the value of 'letter' to the screen
        System.out.println(letter);
    }
}