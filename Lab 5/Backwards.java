public class Backwards {

    public static void main(String args[]) {
        
        String s1 = new String("pay no attention to that man behind the curtain");
        
        System.out.println(s1.substring    (40));
        System.out.println(s1.substring(36, 39));
        System.out.println(s1.substring(29, 35));
        System.out.println(s1.substring(25, 28));
        System.out.println(s1.substring(20, 24));
        System.out.println(s1.substring(17, 19));
        System.out.println(s1.substring (7, 16));
        System.out.println(s1.substring  (4, 6));
        System.out.println(s1.substring  (0, 3));
        
    }
}